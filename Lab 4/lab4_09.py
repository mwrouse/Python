print("Tell me three numbers and I can tell you which is smaller!\n\n")

firstNumber = int(input("First number: "))
secondNumber = int(input("Second number: "))
thirdNumber = int(input("Third number: "))

#variable for comparison and printing the smallest number
smallerNumber = 0

if firstNumber > secondNumber:
    smallerNumber = secondNumber
else:
    smallerNumber = firstNumber

if smallerNumber > thirdNumber:
    smallerNumber = thirdNumber

print("The smallest of those three numbers is", smallerNumber)

print("\n\nDone...")
